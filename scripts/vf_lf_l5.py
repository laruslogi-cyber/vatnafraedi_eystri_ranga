print(f"Q50 (miðgildi):    {Q50:.2f} m³/s")
print(f"Q95 (lágrennsli):  {Q95:.2f} m³/s")
print(f"Q5/Q95 hlutfall:   {Q5/Q95:.2f}")
print(f"Hámarksrennsli:    {Q.max():.2f} m³/s")
print(f"Lágmarksrennsli:   {Q.min():.2f} m³/s")
print("="*50)