def load_and_prep_data(filepath):
    df = pd.read_csv(filepath, sep=';')
    df = df.rename(columns={'YYYY': 'year', 'MM': 'month', 'DD': 'day'})
    df['date'] = pd.to_datetime(df[['year', 'month', 'day']])

    # Sía dagsetningar
    mask = (df['date'] >= '1993-10-01') & (df['date'] <= '2023-09-30')
    df = df[mask].copy().reset_index(drop=True)

    # Bæta við vatnsári (Water Year)
    df['water_year'] = df['date'].apply(lambda d: d.year + 1 if d.month >= 10 else d.year)
    return df

def lyne_hollick(Q, alpha=0.925, n_passes=3):
    """Baseflow separation – Lyne-Hollick aðferð."""
    Q = np.array(Q, dtype=float)
    n = len(Q)

    for i in range(n_passes):
        qf = np.zeros(n)
        if i % 2 == 0:  # Áfram (Forward pass)
            qf[0] = Q[0] / 2
            for t in range(1, n):
                qf[t] = alpha * qf[t-1] + (1 + alpha) / 2 * (Q[t] - Q[t-1])
                qf[t] = np.clip(qf[t], 0, Q[t])
        else:           # Afturábak (Backward pass)
            qf[-1] = Q[-1] / 2
            for t in range(n-2, -1, -1):
                qf[t] = alpha * qf[t+1] + (1 + alpha) / 2 * (Q[t] - Q[t+1])
                qf[t] = np.clip(qf[t], 0, Q[t])

        Q_quick = qf

    Qb = np.clip(Q - Q_quick, 0, Q)
    return Qb

def find_recession_periods(Q, min_length=10, max_ratio=0.99):
    """Finnur lækkunartímabil í rennsli."""
    periods = []
    n = len(Q)
    i = 0
    while i < n - min_length:
        if Q[i+1] < Q[i]:
            j = i + 1
            while j < n and Q[j] <= Q[j-1] * max_ratio + 0.01:
                j += 1
            if j - i >= min_length:
                periods.append((i, j))
            i = j
        else:
            i += 1
    return periods

def calculate_k_for_period(Q, start, end):
    """Reiknar recession constant (k) fyrir tiltekið tímabil."""
    q_rec = Q[start:end]
    t = np.arange(len(q_rec))
    slope, intercept = np.polyfit(t, np.log(q_rec), 1)
    k = -1 / slope if slope != 0 else np.nan
    return k, slope, intercept, q_rec, t


