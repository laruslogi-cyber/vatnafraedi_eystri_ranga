# 2. KEYRSLA OG GREINING (Execution)
# ─────────────────────────────────────────────
# Lesa gögn
df = load_and_prep_data(FILE_PATH)
Q = df['qobs'].values
dates = df['date'].values

print(f"Fjöldi daga: {len(df)}")
print(f"Meðalrennsli: {Q.mean():.2f} m³/s | Hámark: {Q.max():.2f} m³/s | Lágmark: {Q.min():.2f} m³/s")

# Baseflow Separation
df['Qb'] = lyne_hollick(Q, alpha=0.925)
df['Qf'] = df['qobs'] - df['Qb']

print("\n--- Baseflow Separation ---")
print(f"Meðal grunnrennsli (Qb): {df['Qb'].mean():.2f} m³/s")
print(f"Meðal yfirborðsrennsli (Qf): {df['Qf'].mean():.2f} m³/s")

# Baseflow Index (BFI) - Optimized with Pandas Groupby
bfi_series = df.groupby('water_year')['Qb'].sum() / df.groupby('water_year')['qobs'].sum()
bfi_df = bfi_series.reset_index(name='BFI')
mean_bfi = bfi_df['BFI'].mean()
print(f"Meðal BFI: {mean_bfi:.3f}")

# Recession Analysis
recession_periods = find_recession_periods(Q, min_length=10)

# Reikna k fyrir ÖLL tímabil til að fá meðaltal/miðgildi (Fixing the bug)
all_k_values = [calculate_k_for_period(Q, s, e)[0] for s, e in recession_periods]
all_k_values = [k for k in all_k_values if k > 0] # Filter out anomalies
mean_k = np.mean(all_k_values)
median_k = np.median(all_k_values)

print(f"\n--- Recession Analysis ---")
print(f"Fjöldi lækkunartímabila: {len(recession_periods)}")
print(f"{'#':<5} {'Upphaf':<15} {'Endir':<15} {'Lengd (dagar)'}")
print("-" * 50)
for i, (start, end) in enumerate(recession_periods):
    print(f"{i+1:<5} {str(dates[start])[:10]:<15} {str(dates[end-1])[:10]:<15} {end - start}")

# Gögn fyrir VALIÐ tímabil
idx = VALIÐ_TÍMABIL - 1
start_idx, end_idx = recession_periods[idx]
k_val, slope, intercept, q_rec, t_rec = calculate_k_for_period(Q, start_idx, end_idx)
q_fit = np.exp(intercept) * np.exp(slope * t_rec)
dates_rec = dates[start_idx:end_idx]

d_start = pd.Timestamp(dates[start_idx]).date()
d_end = pd.Timestamp(dates[end_idx-1]).date()

print(f"\nValið tímabil #{VALIÐ_TÍMABIL}: {d_start} → {d_end}")
print(f"Lengd: {end_idx - start_idx} dagar | Recession constant (k): {k_val:.1f} dagar")


