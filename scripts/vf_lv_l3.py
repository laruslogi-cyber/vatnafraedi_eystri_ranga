axes[0].plot(dates_rec, q_rec, 'b-o', markersize=4, label='Mælt rennsli')
axes[0].plot(dates_rec, q_fit, 'r--', linewidth=2, label=f'Fitted: Q₀·e^(-t/{k_val:.1f})')
